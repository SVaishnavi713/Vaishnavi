
public class operators {

	public static void main(String[] args) {
		
		// declare variables and initiate values
		int val1=10;
		int val2=20;
		
		// Arithmetic Operators
		int addresult=val1+val2;
		int subresult=val1-val2;
		int mulresult=val1*val2;
		int divresult=val1/val2;
		int modresult=val1%val2;
		System.out.println("add:" +addresult);
		System.out.println("sub:" +subresult);
		System.out.println("mul:" +mulresult);
		System.out.println("div:" +divresult);
		System.out.println("mod:" +modresult);
		

	}

}
